#include <stdio.h>

int main(int argc, char * argv[])
{
    int A,B;
    scanf("%d %d", &A, &B);
    printf("%d\n",A+B);
    printf("%d\n",A-B);
    printf("%d\n",A*B);
    printf("%d\n",A/B);
    printf("%d\n",A%B);
    return 0;
}
